char	*ft_strlowcase(char *str)
{
	int	i;
	int	tmp;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A') && (str[i] <= 'Z'))
		{
			tmp = str[i];
			str[i] = tmp + 32;
		}
		i++;
	}
	return (str);
}
